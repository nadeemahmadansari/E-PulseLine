<p style="font-size:15px;">
Dr. Reddy received his medical degree from the Stanley Medical College in Chennai and later trained as a Cardiologist in the UK and USA. He did his Fellowship from the Massachusetts General Hospital, Boston and went on to head several research programs at the Missouri State Chest Hospital, USA where he worked for several years before returning to India in 1978, at the behest of his father.

Dr Reddy's pioneering success includes the initiation of a change towards preventive care, by pushing for a new standard in assessing and diagnosing India's health to fight the triple terrors of diabetes, heart diseases and cancer through preventive health checks. Telemedicine, as in the world's first V-SAT enabled village, and health insurance like the Aragonda Re. 1/day scheme, were other successful pilots.

As the Chairman of the CII National Healthcare Committee since 2006, Dr Reddy constituted sub-committees for drawing up standards for accreditation of Indian Hospitals for the very first time in the country. Ever since, the committees have done commendable work in the areas of medical value travel and medical code of ethics. One of the areas of significant impact has been in the area of budget recommendations to the Government and development of Health Insurance

He received a number of esteemed awards and recognitions such as Mother St Teresa's 'Citizen of the Year Award', recognition in Harvard School publications for pioneering efforts in healthcare, 'Life Time Achievement Award' by Hospimedica International, the 'Asia Pacific Bio Business Leadership Award' by the University of Southern California, Modern Medicare Excellence Award by the ICICI Group for outstanding achievements in the healthcare industry and numerous others.

Dr Prathap C Reddy was conferred with second highest civilian award, the 'Padma Vibhushan' in March 2010. This unequalled commendation from the Government of India is an acknowledgement of his untiring pursuit for excellence in healthcare as Apollo strives towards touching a billion lives.
