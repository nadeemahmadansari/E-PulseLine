<h2>Dr. Mahendra Bhandari</h2>
<br/>
<h4>M.B.B.S, M.S (Surg.), M.Ch (Uro.), FAMS, FNA.Sc, D.Sc(Hon), MBA
Director Robotic Surgery Research, Vattikuti Urology Institute
Henry Ford Hospital, Detroit, Michigan
Chief Executive Officer, Vattikuti Foundation, Southfield, Michigan</h4>
<br/>
<p style="font-size:15px;">
A renowned urological surgeon with over 40 years of experience, Dr Bhandari joined Vattikuti Urology Institute (VUI) Henry Ford Hospital in Detroit, Michigan in 2006. At the VUI Dr. Bhandari focused on developing a clinical research program and organized the largest database of robotic radical prostatectomy in the world. He coordinated the development of new robotic surgical applications for kidney surgery and the robotic surgery in children. Currently, he is actively promoting the development of robotic surgery in India.

While in India, he trained in urology at the prestigious Christian Medical College,Vellore. He has worked in senior capacities at the SMS Medical College, Jaipur, All India Institute of Medical Science, New Delhi (AIIMS), JIPMER Pondicherry, Sanjay Gandhi Postgraduate Institute, Lucknow and at the University of Florida, Gainesville.

He was appointed as the Director of Sanjay Gandhi Postgraduate Institute of Medical Sciences for two terms and was the Vice Chancellor of King Georges Medical University, Lucknow. He founded the first virtual medical university at the Apollo Hospitals in 2001 branded as Medvarsity.

Dr. Bhandari served as the President of the Urological Society of India, Indian Society of organ transplantation and the Asian Society of Transplantation. He founded the prestigious Indian Journal of Urology and was the first editor from 1984-1994. The Journal is a peer reviewed, indexed, open-access and widely read source of urological literature. He served on the editorial board of several prestigious journals including the British Journal of Urology.

He is the founder of the Center of the Biomedical Magnetic Resonance Institute at Lucknow, India and is an Honorary Professor at the Institute.

Dr. Bhandari was awarded the prestigious Dr B.C Roy award in the year 1996 and the Padma Shri by the Government of India in 2000. A medical graduate from the SMS Medical College Jaipur, Dr. Bhandari is also a graduate in Biostatistics from the Harvard University and an MBA from in Ross School of Business, University of Michigan. Currently, he is a senior staff and the Director of Robotic Surgery Research at the Vattikuti Urology Institute and the CEO of the Vattikuti Foundation, Michigan U.S.
</p>
