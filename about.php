<div class="col-sm-10" style="min-height:700px;">
<p style="margin-left:30px;font-size:20px;margin-top:20px;">The seeds of the first Homoeopathic Medical College of Lucknow were sown in 1912 by Dr. G.N.
Ohdedar which existed till 1920 when Dr. B.S. Tandon of Lucknow did a herculean task by
establishing National Homoeopathic Medical College and Hospital in 1921. Later on it was
registered under Society Registration Act 21 1860. The first Principal of this college was Dr. J.C.
Das, who was the direct disciple of Dr. J.T. Kent of Chicago, USA.The first course started in
N.H.M.C. was two year H.M.B which was later upgraded to three year from 1940 to 1949. In the
year 1950, a four year diploma course of B.M.S. was started which was later affiliated to
Homoeopathic Medicine Board in 1954. It was provincial zed by Uttar Pradesh Government in
the year 1968. The first five year degree course known as G.H.M.S. was commenced in 1958
and the college was affiliated to Agra University Agra. N.H.M.C. became Asia’s first degree
college of Homoeopathy in 1961.</p>
<br/>
<p style="margin-left:30px;font-size:20px;">
The college was disaffiliated by Agra University Agra in 1966 due to nonconformity of minimum
standards and again B.M.S. diploma course was enforced. It was in the year 1971 when the
college was affiliated to Kanpur University Kanpur and G.H.M.S. degree course was reinstated
which continued till 1980. To give uniformity, the first B.H.M.S. five and half year degree course
was started in 1981 for which admissions were taken through C.P.M.T. At that point of time the
college was again affiliated to erstwhile Agra University Agra which was later named as Bhim
Rao Ambedkar University Agra. The admission capacity to this college is 50. The college
continued in the same old rented building situated in Cant Road kaiserbag, Lucknow.
</p>
<br/>
<p style="margin-left:30px;font-size:20px;">
It was historical event when the Govt. of Uttar Pradesh sanctioned Rs. 1611.31 lakh for
purchasing five acre land in Gomti Nagar and for construction of new building to house
N.H.M.C. The new premises was inaugurated by no less than the then Prime Minister of India,
Sri Atal Bihari Bajpai on 27th Feb 2000. The present campus has five storied educational cum
Administrative block, two storied OPD block, four storied girls’ and boys’ hostel and quarter for
teaching and non-teaching staff. Another landmark achievement is the installation of our
Master Dr. HAHNEMANN 12 feet bronze statue with 10 feet elevation total 22 feet on the main
crossing near State National Homoeopathic Medical College named as Hahnemann Chauraha.
</p>
